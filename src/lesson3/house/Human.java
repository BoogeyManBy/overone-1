package lesson3.house;

public class Human {
    int height;
    String name;
    int age;
    boolean isAlive;

    public Human(int height, String name, int age, boolean isAlive) {
        this.height = height;
        this.name = name;
        this.age = age;
        this.isAlive = isAlive;
    }

    Human() {}
}
